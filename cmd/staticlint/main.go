// Анализатор содержит все стандартные статические анализаторы пакета
// [https://pkg.go.dev/golang.org/x/tools/go/analysis/passes], все анализаторы класса SA пакета [https://staticcheck.io/],
// а также S1005, ST1005 и QF1005. Кроме того, выполняется проверка анализатором [https://github.com/timakin/bodyclose].
// Также выполняется проверка прямого вызова os.Exit в функции main пакета main.
//
// Сборка: go build -o staticlint ./cmd/staticlint
//
// Запуск: staticlint ./...
